using System;
using Calculator;

namespace Calculator

    public class Calculator01
    {
        public float numero1;
        public float numero2;

        public float Somar()
        {
            return numero1 + numero2;
        }

        public float Subtrair()
        {
            return numero1 - numero2;
        }

        public float Multiplicar()
        {
            return numero1 * numero2;
        }

        public float Dividir()
        {
            return numero1 / numero2;
        }
    }

